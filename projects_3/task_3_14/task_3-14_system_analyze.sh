#!/bin/bash

echo "Файловая система - Заполнение:"
echo "------------------------------"

df -h | awk 'NR>1 {print $1 " - " $5}'

echo
echo "Проверка на заполнение > 90%:"
echo "-----------------------------"

df -h | awk 'NR>1 {
    
    gsub(/%/, "", $5)
    if ($5 > 90) {
        print "ВНИМАНИЕ: " $1 " заполнена на " $5 "% !"
    }
}'
